#include "ft_printf.h"

char     *ft_toupper(char* s)
{
    int i;

    i = 0;
    while (s[i])
    {
        if (s[i] >= 'a' && s[i] <= 'z')
            s[i] = s[i] - 32;
        i++;
    }
    return (s);
}